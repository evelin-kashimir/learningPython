print('---- CALCULADORA INTELIGENTE -----')
valor1 = int(input('Informe um numero inteiro: '))
valor2 = int(input('Informe um outro numero inteiro: '))

operacao = valor1 + valor2 
print('SOMA')
print(valor1," + ",valor2," = ",operacao)

operacao = valor1 - valor2 
print('SUBTRAÇÃO')
print(valor1," - ",valor2," = ",operacao)

operacao = valor1 * valor2 
print('MULTIPLICAÇÃO')
print(valor1," x ",valor2," = ",operacao)

operacao = valor1 / valor2 
print('DIVISÃO')
print(valor1," / ",valor2," = ",operacao)
